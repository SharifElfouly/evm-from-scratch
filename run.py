from cpu import CPU

# PROGRAM = [0x60, 0x42, 0x60, 0x01, 0x53, 0x00]
PUSH = [0x62, 0xFF, 0xFF, 0xFF, 0x60, 0xFF]
MSTORE = [0x52, 0x60, 0xFF, 0x60, 0x1C, 0x53]
MLOAD = [0x60, 0x00, 0x51]
ADD = [0x01]
END = [0x00]

PROGRAM = PUSH + MSTORE + MLOAD*2 + ADD
# PROGRAM = PUSH + ADD
PROGRAM += END

cpu = CPU()
cpu.load(PROGRAM)
cpu.run()
